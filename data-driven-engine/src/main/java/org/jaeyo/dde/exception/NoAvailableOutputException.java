package org.jaeyo.dde.exception;

public class NoAvailableOutputException extends Exception {

	public NoAvailableOutputException() {
		super();
	}

	public NoAvailableOutputException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NoAvailableOutputException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoAvailableOutputException(String message) {
		super(message);
	}

	public NoAvailableOutputException(Throwable cause) {
		super(cause);
	}

}
